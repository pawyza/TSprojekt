    private Client getClient() throws SQLException, ClassNotFoundException {
        Client client = null;
        final String sqlQuery = "SELECT * FROM student";
        try (Connection connection = DbUtil.getInstance().getConnection();
                Statement statement = connection.createStatement();
                ResultSet resultSet = statement.executeQuery(sqlQuery);){
                    while (resultSet.next()) {
                            int clientid = resultSet.getInt("idclient");
                            String forename = resultSet.getString("forname");
                            String surname = resultSet.getString("surname");
                            String pesel = resultSet.getString("pesel");
                            int phoneNumber = resultSet.getInt("phone");
                            String login = resultSet.getString("login");
                            String password = resultSet.getString("password");
                            client = new Client(clientid,forename,surname,pesel,phoneNumber,login,password);
                    }
                    return client;
                }}
